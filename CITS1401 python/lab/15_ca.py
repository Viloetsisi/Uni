class car:
    def __init__(self):
        self.model = ""
        self.rego = ""
        self.year = ""
        self.color = ""

    def setModel(self, model):
        self.model = model

    def getModel(self):
        return self.model

    def setRego(self, rego):
        self.rego = rego

    def getRego(self):
        return self.rego

    def setYear(self, year):
        self.year = year

    def getYear(self):
        return self.year

    def setColor(self, color):
        self.color = color

    def getColor(self):
        return self.color

